    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      try {
        GiangVien gv = new GiangVien();
        gv.setMagv(magvTf.getText());
        gv.setHoten(hotenTf.getText());
        gv.setDiachi(diachiTf.getText());
        gv.setGioitinh(namR.isSelected());
        gv.setSdt(sdtTf.getText());
        Calendar nscal = (Calendar) nsDP.getModel().getValue();
        gv.setNgaysinh(new Date(nscal.getTimeInMillis()));
        if (gvdao.update(gv)) {
          MessageBox.showInfoMessage(this, "Thông tin giảng viên đã được cập nhật!");
        } else {
          throw new RuntimeException("Không thể cập nhật thông tin giảng viên!");
        }
      } catch (Exception ex) {
        MessageBox.showErrorMessage(this, ex.getMessage());
      }
    }//GEN-LAST:event_jButton1ActionPerformed

