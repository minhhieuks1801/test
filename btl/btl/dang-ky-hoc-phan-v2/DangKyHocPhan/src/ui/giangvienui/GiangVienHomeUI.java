  private void showInfor() {
    try {
      GiangVien gv = gvdao.get(LoginContext.getUser().getUsername());
      magvTf.setText(gv.getMagv());
      hotenTf.setText(gv.getHoten());
      diachiTf.setText(gv.getDiachi());
      khoaTf.setText(gv.getKhoa().getTenKhoa());
      namR.setSelected(gv.isGioitinh());
      nuR.setSelected(!gv.isGioitinh());
      LocalDate ns = gv.getNgaysinh().toLocalDate();
      nsDP.getModel().setDate(ns.getYear(), ns.getMonthValue() - 1, ns.getDayOfMonth());
      nsDP.getModel().setSelected(true);
      sdtTf.setText(gv.getSdt());
    } catch (Exception ex) {
      MessageBox.showErrorMessage(this, ex.getMessage());
    }

  }

